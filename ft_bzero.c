#include "libft.h"
/*The bzero() function erases the data in the n bytes
of the memory starting at the location pointed to by s,
by writing zeros(bytes containinn '\0') to that area.*/
void	ft_bzero(void *s, size_t n)
{
	unsigned char	*p;
	size_t			i;

	p = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		*p++ = 0;
		i++;
	}
}
