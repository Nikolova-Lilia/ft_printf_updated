#include "libft.h"
/*The strlen() function calculates the length
of the string pointed to by s, excluding
the terminating null byte ('\0').*/
size_t	ft_strlen(const char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return ((size_t)i);
}
